// 매칭 후보를 이름 길이 내림차순으로 정렬 (긴 이름을 먼저 검사해 짧은 모호 이름의
// 오매칭을 방지).
const MATCH_CANDIDATES = [
  ...Object.keys(LAWD_CODES).map((name) => ({ name, kind: "unique" })),
  ...AMBIGUOUS_GU.map(([name]) => ({ name, kind: "ambiguous" })),
].sort((a, b) => b.name.length - a.name.length);

/**
 * 주소 문자열에서 구/군/시 이름을 찾아 LAWD_CD를 반환. 못 찾으면 null.
 * 예: "서울특별시 성동구 성수동1가 685" → "11200"
 */
export function addressToLawdCd(address) {
  for (const { name, kind } of MATCH_CANDIDATES) {
    if (!address.includes(name)) continue;
    if (kind === "unique") return LAWD_CODES[name];

    const [, cityMap] = AMBIGUOUS_GU.find(([g]) => g === name);
    const cityHit = cityMap.find(([city]) => address.includes(city));
    if (cityHit) return cityHit[1];

    // 시/도명이 주소에 전혀 없으면 서울을 기본값으로 사용 (중구/강서구만 서울 옵션 있음).
    // 시/도명이 있지만 이 구 이름과 매칭되는 시/도가 아니면(예: 폐지된 "인천 중구"),
    // 잘못된 지역으로 추정하지 않고 null을 반환합니다.
    const hasAnyProvince = PROVINCE_MARKERS.some((p) => address.includes(p));
    if (!hasAnyProvince) {
      const seoulHit = cityMap.find(([city]) => city === "서울");
      if (seoulHit) return seoulHit[1];
    }
    return null;
  }
  return null;
}

/** 오늘 기준 "가장 최근에 데이터가 있을 법한" YYYYMM (통상 실거래가는 1~2개월 지연 공시) */
export function recentDealYmd(monthsAgo = 1) {
  const d = new Date();
  d.setMonth(d.getMonth() - monthsAgo);
  const y = d.getFullYear();
  const m = String(d.getMonth() + 1).padStart(2, "0");
  return `${y}${m}`;
}
